      <h2>挺同VS.反同 公投戰場交鋒</h2>
      <p><br></p>
      <p>去年5月24日，司法院大法官做出釋字第748號解釋，認為民法未保障同性婚姻違憲，要求立法機關在兩年內修法，保障同婚權益。</p>
      <p><br></p>
      <p>不過，大法官的釋憲引發反同婚團體不滿，「下一代幸福聯盟」等團體發起3項「愛家公投」，包括「民法婚姻定義應為一男一女」、「反對同性婚姻入民法，應另立專法」、「國中及國小不應進行同志教育」。</p>
      <p><br></p>
      <p>為反制愛家公投，婚姻平權大平台等挺同團體隨即提出「以民法保障同性婚姻」、「支持性別平等教育」2項公投。</p>
      <p><br></p>
      <p><b>雖然不論公投案結果為何，都不會影響大法官釋憲結果，2019年5月同性伴侶一定可以結婚。</b>但對於應該用修改民法還是另立專法的形式，保障同性婚姻，社會上仍意見分歧。在修民法與立專法間，候選人的態度又為何？</p>
