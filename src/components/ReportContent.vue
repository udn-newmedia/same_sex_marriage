      <h2>丁守中、李錫錕主張立專法 姚文智支持修民法</h2>
      <p><br></p>
      <p>國民黨台北市長候選人<span style="background-color: #f0e610">丁守中</span>接受《聯合報》專訪時表示，他尊重每一個人愛的選擇，也祝福有情人終成眷屬。但要修改民法會大動干戈，社會大眾也還沒準備好。「只要對同志婚姻平權有合法保障就可以，我主張用專法來保障。」他也再度明確表態支持愛家三公投。</p>
      <p><br></p>
      <p>無黨籍台北市長候選人<span style="background-color: #f0e610">李錫錕</span>表示，同志婚姻平權是一個「不必討論」的議題，而是在民主社會自然而然應該有的觀念，要尊重包容每個人的生存權利和選擇。不過他認為，台灣社會尚處過渡階段，因此他主張先立專法。</p>
      <p><br></p>
      <p>民進黨台北市長候選人<span style="background-color: #f0e610">姚文智</span>日前表態支持同性婚姻公投。在北市長選舉首場辯論中，他提到立院修法過程中，他參與民法修法連署，同樣也是台北市長候選人中唯一呼應社民黨對同志婚姻的主張。在婚姻平權大平台的問卷中，姚文智也表態支持民法修改。</p>
