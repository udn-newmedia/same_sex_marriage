      <p><br></p>
      <p>苗博雅認為，主張另訂專法的人士，實際上連半條草案都提不出來，根本是「空氣專法」，當然很多人會質疑專法可以給同性伴侶什麼保障。「請專法派先把版本拿出來，大家才有討論的基礎。」</p>
      <p><br></p>
      <p>根據平權公投小組統計，平權公投有近四成連署來自雙北，75%來自六都，其中又以年輕人居多，可能因此影響年輕人的投票率及投票意向。九合一大選綁公投，候選人對同婚議題的立場，會如何影響選民的投票意向，有待選舉結果出爐後進一步觀察。</p>
